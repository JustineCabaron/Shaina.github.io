            <h4>History of Pastry</h4>

            <p class="p1">Pastry as we know it today – in all its crumbly, flaky, buttery glory – is a far cry from where it all began. With so many regional variations and international influences putting their marks on pastry in Britain, this humble dough has taken a truly remarkable journey through the culinary world.</p>

            <h5>Distant origins</h5>

            <p>As usual, many of the greatest creations in the world can be traced back to ancient and classical times. The ancient Egyptians gave us hieroglyphics and papyrus, the Greeks philosophy and democracy, the Romans military tactics and aqueducts (a whistle-stop tour of history), but it is thought they also all used types of pastry. Utilising the natural sweeteners available, it is believed that the ancient Egyptians, Greeks and Romans all used a filo-type pastry (a simple mix of flour and oil) to create honey cakes, fruit pastries, sweet tarts and dumplings stuffed with dates and nuts. Evidence of these sweet treats can be seen on ancient tomb paintings or mentioned in writings such as in the comic plays of Aristophanes.</p>

            <p>Moving forwards the Romans created a basic pastry dough that was used as a covering for meats when cooking. The plain paste of flour, oil and water was used as a shield to keep the meat moist and also protected the outside from burning – it was never meant to be eaten and was subsequently discarded. It seems that the main factor holding back ancient bakers (they were not wanting for ingenuity or creativity) was the lack of butter or lard to create the richness we associate with pastry today.</p>

            <h5>Medieval masterpieces and French fancies</h5>

            <p>It’s not until the late Medieval period that we start to see fuller pastry recipes that resemble the shortcrust and puff versions we are more familiar with. The Forme of Cury is the earliest known English language cookbook, thought to have been written at the end of the fourteenth century, and it refers to the making of chastletes and coffins – early pies and tart cases. A lot of these early recipes, particularly through the fifteenth century, include egg yolks and saffron to add colour to the dough. From here we get the development of the hot water crust pastry and hand-formed game pies became popular as table centre-pieces, and were often associated with banquets and galas.</p>

            <p>In the seventeenth century, pastry-making became increasingly fashionable – British bakers prided themselves on the techniques and elaborate decorations used for their creations, and were inspired further by the culinary developments happening across the Channel. Puff pastry was supposedly created by French painter and apprentice cook, Claude Gelée, in about 1645, when he accidently created a laminated dough when trying to make a form of rolled butter cake for his sick father. The earliest British version recorded is from 1692, with a hand-written manuscript describing Hannah Bisaker’s recipe for ‘puff paist’, which tells the reader to ‘lay some Butter in litle Pecies’ over the flour dough, before folding and rolling three times.</p>

            <p>French pastry master, Marie-Antoine Carême (1784–1833), is widely considered to be the first ‘celebrity chef’, bringing pastry into the world of grande cuisine. His elaborate creations graced the windows of his Paris patisserie shop, before he went on to cook for European leaders including George IV. The glamour and splendour associated with these rich, buttery confections helped grow the popularity of French pastry desserts as the epitome of fine dining. We certainly have a lot to thank them for, from Carême’s development of the layered Mille-Feuille (a ‘thousand leaves’) and the kitchen mishap that resulted in the tarte Tatin, to the traditional fillings of crème pâtissière and frangipane, many of our modern recipes can be linked to the kitchens of France.</p>
