          <h4>Pastry Introduction</h4>
          <img src="img/gif1.gif" class="gif1">

          <p>Pastry is essentially a type of bread and so many different types exist that there is no one way to classify them. Their chief differences have to do with their fat, the type used, its proportion, and how it is introduced it into the flour. The two main types of classification used here are: NONLAMINATED PASTRY, where solid fat is cut into the flour, or added as melted or liquid oil; or LAMINATED PASTRY, where solid fat is repeated folded into the dough using a technique called lamination. Those include Pie and Tart Crusts, Choux or Pâte à Choux, Brioche, Croissant, Danish, Puff Pastry, Phyllo, and Strudel.</p>

          <p>All pastries have the minimum of ingredients - flour, liquid (water or milk), salt and fat (usually butter, but can be shortening, lard or oil) - in different proportions by weight, being handled and baked in various ways. Additional ingredients are found in some recipes, such as eggs and sugar, contribute to leavening, flavor and browning. Except for croissant, Danish and brioche, which make use of yeast, most leavening agents for pastries are steam and air. They are decorated - braided, shaped, filled - in a myriad of ways.</p>

