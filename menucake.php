            <p class="p1"><strong>So what is cake?<img src="cakes/bday.jpeg"></strong></p>

            <p><i>Cake</i> is a form of sweet food made from flour, sugar, and other ingredients, that is usually <i>baked</i>. In their oldest forms, cakes were modifications of <i>bread</i>, but cakes now cover a wide range of preparations that can be simple or elaborate, and that share features with other <i>desserts</i> such as <i>pastries</i>, <i>meringues</i>, <i>custards</i>, and 
            <i>pies</i>.</p>

            <p>The most commonly used cake ingredients include <i>flour, sugar, eggs, butter or oil or margarine, a liquid, and a leavening agent, such as baking soda or baking powder</i>. Common additional ingredients and flavourings include <i>dried, candied, or fresh fruit, nuts, cocoa,</i> and extracts such as <i>vanilla,</i> with numerous substitutions for the primary ingredients. Cakes can also be filled with <i>fruit preserves, nuts or dessert sauces (like pastry cream), iced with buttercream or other icings, and decorated with marzipan, piped borders, or candied fruit.</i></p>

            <p>Cake is often served as a celebratory dish on ceremonial occasions, such as weddings, anniversaries, and birthdays. There are countless cake recipes; some are bread-like, some are rich and elaborate, and many are centuries old. Cake making is no longer a complicated procedure; while at one time considerable labor went into cake making (particularly the whisking of egg foams), baking equipment and directions have been simplified so that even the most amateur of cooks may bake a cake.</p>
            <hr>

            <p class="p1"><strong>History</strong><img src="cakes/bday2.jpeg"></p>

            <p>The term <i>"cake"</i> has a long history. The word itself is of <i>Viking origin</i>, from the Old Norse word <i>"kaka"</i>.</p>

            <p>The ancient Greeks called cake <i>πλακοῦς (plakous)</i>, which was derived from the word for 
            <i>"flat", πλακόεις (plakoeis)</i>. It was baked using flour mixed with eggs, milk, nuts, and honey. They also had a cake called <i>"satura",</i> which was a flat heavy cake. During the Roman period, the name for cake became <i>"placenta"</i> which was derived from the Greek term. A placenta was baked on a pastry base or inside a pastry case.</p>

            <p>The Greeks invented beer as a leavener, frying fritters in olive oil, and cheesecakes using goat's milk.[4] In ancient Rome, the basic bread dough was sometimes enriched with butter, eggs, and honey, which produced a sweet and cake-like baked good. Latin poet Ovid refers to his and his brother's birthday party and cake in his first book of exile, <i>Tristia.</i></p>

            <p>Early cakes in England were also essentially bread: the most obvious differences between a 
            <i>"cake" and "bread"</i> were the round, flat shape of the cakes, and the cooking method, which turned cakes over once while cooking, while bread was left upright throughout the baking process.
            </p>
            <hr>

            <p class="p1"><strong>Varieties</strong><img src="cakes/butter.jpeg"></p>

            <p>Cakes are broadly divided into several categories, based primarily on ingredients and mixing techniques. Although clear examples of the difference between cake and bread are easy to find, the precise classification has always been elusive.</p>

            <h3><strong>Butter Cake</strong></h3>

            <p>Butter cakes are made from creamed butter, sugar, eggs, and flour. They rely on the combination of butter and sugar beaten for an extended time to incorporate air into the batter.</p>

            <p>A classic pound cake is made with a pound each of butter, sugar, eggs, and flour. Another type of butter cake that takes its names from the proportion of ingredients used is 1-2-3-4 cake: 1 cup butter, 2 cups sugar, 3 cups flour, and 4 eggs.[12] According to Beth Tartan, this cake was one of the most common among the American pioneers who settled North Carolina.</p>

            <p>Baking powder is in many butter cakes, such as Victoria sponge. The ingredients are sometimes mixed without creaming the butter, using recipes for simple and quick cakes.</p>

            <h3><strong>Sponge Cake</strong><img src="cakes/sponge.jpeg"></h3>

            <p>Sponge cakes (or foam cakes) are made from whipped eggs, sugar, and flour. Traditional sponge cakes are leavened only with eggs. They rely primarily on trapped air in a protein matrix (generally of beaten eggs) to provide leavening, sometimes with a bit of baking powder or other chemical leaven added. Egg-leavened sponge cakes are thought to be the oldest cakes made without yeast.</p>

            <p>Angel food cake is a white cake that uses only the whites of the eggs and is traditionally baked in a tube pan. The French Génoise is a sponge cake that includes clarified butter. Highly decorated sponge cakes with lavish toppings are sometimes called gateau, the French word for cake. Chiffon cakes are sponge cakes with vegetable oil, which adds moistness.</p>

            <h3><strong>Chocolate Cake</strong><img src="cakes/chocolate.jpeg"></h3>            

            <p>Chocolate cakes are butter cakes, sponge cakes, or other cakes flavored with melted chocolate or cocoa powder. German chocolate cake is a variety of chocolate cake. Fudge cakes are chocolate cakes that contain fudge.</p>
            <br><br><br><br><br>

            <h3><strong>Chocolate Cake</strong><img src="cakes/coffee.jpeg"></h3>

            <p>Coffee cake is generally thought of as a cake to serve with coffee or tea at breakfast or a coffee break. Some types use yeast as a leavening agent while others use baking soda or baking powder. These cakes often have a crumb topping called streusel or a light glaze drizzle.</p>
            <br><br><br><br><br><br><br>

            <h3><strong>Flourless Cake</strong><img src="cakes/flourless.jpeg"></h3>

            <p>Baked flourless cakes include baked cheesecakes and flourless chocolate cakes. Cheesecakes, despite their name, are not cakes at all. Cheesecakes are custard pies, with a filling made mostly of some form of cheese (often cream cheese, mascarpone, ricotta, or the like), and have very little flour added, although a flour-based or graham cracker crust may be used. Cheesecakes are also very old, with evidence of honey-sweetened cakes dating back to ancient Greece.</p>
            <br><br><br><br><br><br><br>

            <h3><strong>Layer Cake</strong><img src="cakes/layer.jpeg"></h3>

            <p>Layer cakes are cakes made with layers of sponge or butter cake, filled with cream, jam or other filling to hold the layers together.</p>
            <br><br><br><br><br><br><br>

            <h3><strong>One Egg Cake</strong><img src="cakes/oneegg.jpeg" class="img1"></h3>

            <p>One egg cakes are made with one egg. They can be made with butter or vegetable shortening. One egg cake was an economical recipe when using two eggs for each cake was too costly.</p>
