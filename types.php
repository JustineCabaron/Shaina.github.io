			<td class="td3">PUFF PASTRY</td>
			<td class="td4">
				<p>The traditional puff pastry has been around for generations and is the base of many wonderful pastries. The classic puff pastry is also known as Pâte feuilletée. In French, it means layered dough (laminated dough). The layers are made with just three main ingredients flour, water, and butter. And there is no leavening or yeast in this dough.</p>

				<p>The puff pastry is made up of three parts -- The flour knows as the détrempe in French, the butter block which is known as the beurrage, and the Pâton, which means the package of dough by combining the two, flour and butter block into one.</p>

				<p>We use it to make savory dishes like chicken pot pie and quick puff pastry pizzas, as well as in desserts like Napoleon, Nutella puffs apple turnovers, and more</p>

				<p>Often you will also see a recipe for quick puff pastry. This is basically a rough mixture of flour and butter combined. We do create a few layers but it is not the same as the classic pastry. It is quicker because it does not need any lamination time. The resulting pastry does turn out buttery and flaky but without even layers of pastry. It tastes delicious and very close to the classic. Definitely a great substitute if you can't make the classic.</p>
			</td>
