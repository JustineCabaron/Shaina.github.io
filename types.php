			<td class="td3">DANISH PASTRY</td>
			<td class="td4">
				<p>Danish pastry is a buttery, flaky, pastry made with a lamination similar to croissants and puff pastry. But, they are sweeter and have more butter and eggs. The Danish dough is not necessarily difficult, but it is time-consuming because we need to chill the dough in between steps.</p>

				<p>It's also a very forgiving dough that's easy to knead with a stand mixer or by hand. There are three main components to this pastry.</p>

				<li>The dough - this is simple yeast-based dough with eggs, butter, and sugar. It can be soft and sticky to work with, so don't skip the chilling times.</li>
				<li>Butter block - yes, it is lots of butter, which we cream with a little flour and then laminate between the dough. The purpose of the flour in the butter is to help stabilize it. Most professionals often skip the flour, but I recommend you use it.</li>
				<li>The fillings - usually this often could be cream cheese or pastry cream with a fruit filling or jam. It can also be just a fruit filling or jam.</li>

				<p>Also, there are many ways to shape the Danish pastry. We have covered this dough in great detail from the basic dough to shaping the Danish as well as individual pastries like Danish pinwheels, cream cheese cylinders as well as pastry cream pain au raisin.</p>
			</td>
