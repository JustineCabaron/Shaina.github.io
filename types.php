			<td class="td4">
				<p>'Croissants de Boulanger' is a flaky, buttery French pastry that is a yeast-based dough laminated with layers of butter. And, then, shaped to create its distinct croissant shape. You will get lots of variations of fillings and shapes made from this basic dough. In this blog, I have made the classic croissants asThere are two main components to this dough: well as some variations like ham and cheese croissants and chocolate.</p>

				<p>There are two main components to this dough:</p>

				<li>The dough - this is simple yeast-based dough with a small amount of butter and sugar.</li>
				<li>Butter block - this is lots of butter, which we cream with a little flour and then laminate between the dough.</li>
