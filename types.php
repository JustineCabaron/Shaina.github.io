			<td class="td3">CHOUX PASTRY - PATE A CHOUX</td>
			<td class="td4">
				<p>Choux pastry pronounced 'shoe pastry' is a French dough called pate a choux, which means 'cabbage paste'. Well, apparently, the little baked puffs resemble small cabbages. 
				Hence, the name.</p>

				<p>The basic dough is cooked twice. First, it's cooked on the stovetop. Then, piped into small puffs and baked into the light and airy pastries. You can also fill them to make impressive desserts like cream puffs, profiteroles, and eclairs.</p>

				<li>Cream puffs - Cream puffs are a great way to make impressive desserts that wow your guests. Using homemade choux pastry from scratch, you can fill these with whipped cream for a quick and tasty treat.</li>
				<li>Profiteroles - The classic French dessert profiteroles are made with choux pastry that is light as air. And, they are filled with vanilla pastry cream, whipped cream, or ice cream, and finished with a chocolate glaze.</li>
				<li>Eclairs - The classic eclair also uses the choux pastry dough but we pipe them into long tubular shapes. Filled with pastry cream, whipped cream, or fruit fillings, etc. Often, with a chocolate glaze too.</li>
				<li>Churros - These are deep-fried treats. And yet, do you know that it uses the choux pastry dough as a base? Yes, instead of baking the choux pastry, we put the dough in a piping bag and we deep-fry it until golden and crispy. Often dusted with cinnamon sugar and served with a chocolate sauce or ice creams.</li>
			</td>
