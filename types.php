			<td class="td3">FILO PASTRY</td>
			<td class="td4">
				<p>Of course, let's not forget the flaky filo or phyllo pastry. Filo is an unleavened pastry of very thin delicate sheets of dough. It has less moisture content so it does not rise like puff pastry.</p>

				<p>The filo layers are separated by melted butter or oil. They bake crispy and flaky. Often used in desserts like apple strudel, chocolate cigars, and baklava as well as savory recipes like samosa or crispy tarts.</p>
